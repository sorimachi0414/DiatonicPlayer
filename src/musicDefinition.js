export const parallelScaleChords=(scale)=>{
  //0,2,3,5,7,9,11
  let parallelElem = [2,5,6] //[3,7,9]
  return parallelElem.map((val,i)=>[
    scale[(0+val)%7],
    scale[(2+val)%7],
    scale[(4+val)%7],
    scale[(6+val)%7]
  ])
}


